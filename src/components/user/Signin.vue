<template>
	<v-container>
		<v-layout row>
			<v-flex xs12 sm6 offset-sm3>
				<v-card-text>
					<v-container>
						<form @submit.prevent="onSignin">
							<v-layout row>
								<v-flex xs12>
									<v-text-field
									name="email"
									label="Mail"
									id="email"
									v-model="email"
									type="email"
									required>
									</v-text-field>
									
								</v-flex>
							</v-layout>
							<v-layout row>
								<v-flex xs12>
									<v-text-field
									name="password"
									label="Password"
									id="password"
									v-model="password"
									type="password"
									>
									</v-text-field>
									
								</v-flex>
							</v-layout>

							
							<v-layout row>
								<v-flex xs12>
									<v-btn type="submit">Sign In</v-btn>
								</v-flex>
							</v-layout>
						</form>
					</v-container>
				</v-card-text>
			</v-flex>
		</v-layout>
	</v-container>
</template>

<script>
	export default {
		data(){
			return {
				email: '',
				password: '',
				
			}
		},
		methods: {
			onSignin () {
				this.$store.dispatch('signUserIn',{
					email: this.email,
					password: this.password
				})		
			}
		},
		computed: {
			
			user () {
				return this.$store.getters.user
			}
		
		},
		watch: {
			// si el sigin ha sido exitoso
			user (value) {
				if (value !== null && value !== undefined) {
					this.$router.push('/')
				}
			}
		}

	}
</script>